package Circle;

public class Circle {
    double radius;

    // Constructors can be viewed as special kind of method
    //Purpose of the constructor is to initialize the instance variables
    // while creating the object
    // Constructors need to have the same exact name as the name of the class
    // Constructor does not have a return type (e.g don't use void )
    // Constructors are called implicitly when the object is created with the new keyword

    public Circle (double newRadius) {
        radius = newRadius;

    }

}


